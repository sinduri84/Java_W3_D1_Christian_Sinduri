package Sinduri.A1;

public class Employee {
    private String emplFirstName;
    private String empLastName;
    private int empYearOfBirth;
    private int empSalary;

    public Employee() {
    }

    public String getEmplFirstName() {
        return emplFirstName;
    }

    public void setEmplFirstName(String emplFirstName) {
        this.emplFirstName = emplFirstName;
    }

    public String getEmpLastName() {
        return empLastName;
    }

    public void setEmpLastName(String empLastName) {
        this.empLastName = empLastName;
    }

    public int getEmpYearOfBirth() {
        return empYearOfBirth;
    }

    public void setEmpYearOfBirth(int empYearOfBirth) {
        this.empYearOfBirth = empYearOfBirth;
    }

    public int getEmpSalary() {
        return empSalary;
    }

    public void setEmpSalary(int empSalary) {
        this.empSalary = empSalary;
    }
}
